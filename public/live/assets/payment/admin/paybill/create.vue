<template>
    <div class="container pay-bill-create">
        <!-- Start .header-section -->
        <div class="content-header-section separator">
            <div class="row between-xs">
                <div class="col">
                    <h2 class="content-header__title">
                        {{ this.$func.__("New Bill Payment", "erp") }}
                    </h2>
                </div>
            </div>
        </div>
        <!-- End .header-section -->

        <form action="" method="post" @submit.prevent="SubmitForPayment">
            <div
                class="panel panel-default"
                style="padding-bottom: 0"
            >
                <div class="panel-body">
                    <show-errors :error_msgs="form_errors"></show-errors>

                    <div class="row">
                        <div class="col-sm-4">
                            <div class="form-group">
                                <select-people
                                    @input="getDueBills"
                                    v-model="basic_fields.people"
                                ></select-people>
                            </div>
                        </div>
                        <div class="col-sm-4">
                            <div class="form-group">
                                <label>{{
                                    this.$func.__("Reference NO", "erp")
                                }}</label>
                                <input
                                    type="text"
                                    class="form-control form-contro-sm form-field"
                                    v-model="basic_fields.trn_ref"
                                />
                            </div>
                        </div>
                        <div class="col-sm-4">
                            <div class="form-group">
                                <label
                                    >{{ this.$func.__("Payment Date", "erp")
                                    }}<span class="required-sign"
                                        >*</span
                                    ></label
                                >
                                <datepicker
                                    v-model="basic_fields.payment_date"
                                ></datepicker>
                            </div>
                        </div>
                        <div class="col-sm-4 with-multiselect">
                            <label>{{
                                this.$func.__("Payment Method", "erp")
                            }}</label>
                            <multi-select
                                v-model="basic_fields.trn_by"
                                :options="pay_methods"
                            ></multi-select>
                        </div>
                        <div class="col-sm-4">
                            <label>{{
                                this.$func.__("Transaction From", "erp")
                            }}</label>
                            <select-accounts
                                v-model="basic_fields.deposit_to"
                                :override_accts="accts_by_chart"
                            ></select-accounts>
                        </div>
                        <div class="col-sm-4">
                            <label>{{
                                this.$func.__("Billing Address", "erp")
                            }}</label>
                            <textarea
                                v-model.trim="basic_fields.billing_address"
                                rows="3"
                                class="form-control form-contro-sm form-field"
                                :placeholder="this.$func.__('Type here')"
                            ></textarea>
                        </div>

                        <check-fields
                            v-if="basic_fields.trn_by.id === '3'"
                            @updateCheckFields="setCheckFields"
                        ></check-fields>
                    </div>
                </div>
            </div>

            <div class="table-responsive">
                <!-- Start .crm-table -->
                <div class="table-container">
                    <table class="table form-table">
                        <thead>
                            <tr class="inline-edit-row">
                                <th scope="col" class="col--id">
                                    {{ this.$func.__("Bill No", "erp") }}
                                </th>
                                <th scope="col">
                                    {{ this.$func.__("Due Date", "erp") }}
                                </th>
                                <th scope="col">
                                    {{ this.$func.__("Total", "erp") }}
                                </th>
                                <th scope="col">
                                    {{ this.$func.__("Due", "erp") }}
                                </th>
                                <th scope="col">
                                    {{ this.$func.__("Amount", "erp") }}
                                </th>
                                <th scope="col" class="col--actions"></th>
                            </tr>
                        </thead>
                        <tbody>
                            <tr
                                :key="key"
                                v-for="(pay_bill, key) in pay_bills"
                                class="inline-edit-row"
                            >
                                <td scope="row" class="col--id">
                                    #{{ pay_bill.voucher_no }}
                                </td>
                                <td
                                    class="col--due-date"
                                    :data-colname="
                                        this.$func.__('Due Date')
                                    "
                                >
                                    {{ pay_bill.due_date }}
                                </td>
                                <td
                                    class="col--total"
                                    :data-colname="
                                        this.$func.__('Total')
                                    "
                                >
                                    {{ moneyFormat(pay_bill.amount) }}
                                </td>
                                <td
                                    class="col--due"
                                    :data-colname="this.$func.__('Due')"
                                >
                                    {{ pay_bill.due }}
                                </td>
                                <td
                                    class="col--amount text-right"
                                    :data-colname="this.$func.__('Amount')"
                                >
                                    <input
                                        type="text"
                                        min="0"
                                        :max="pay_bill.due"
                                        v-model="totalAmounts[key]"
                                        @keyup="updateFinalAmount"
                                        class="text-right"
                                    />
                                </td>
                                <td
                                    class="delete-row"
                                    :data-colname="
                                        this.$func.__(
                                            'Remove Above Selection',
                                            'erp'
                                        )
                                    "
                                >
                                    <a @click.prevent="removeRow(key)" href="#"
                                        ><i class="flaticon-trash"></i
                                    ></a>
                                </td>
                            </tr>

                            <tr class="total-amount-row inline-edit-row">
                                <td class="text-right pr-0" colspan="4">
                                    {{ this.$func.__("Total Amount", "erp") }}
                                </td>
                                <td
                                    class="text-right"
                                    :data-colname="
                                        this.$func.__('Total Amount')
                                    "
                                >
                                    <input
                                        type="text"
                                        class="text-right"
                                        :value="moneyFormat(finalTotalAmount)"
                                        readonly
                                        disabled
                                    />
                                </td>
                                <td class="text-right"></td>
                            </tr>
                        </tbody>
                        <tr class="form-group inline-edit-row">
                            <td colspan="9" style="text-align: left">
                                <label>{{
                                    this.$func.__("Particulars", "erp")
                                }}</label>
                                <textarea
                                    v-model="particulars"
                                    rows="4"
                                    maxlength="250"
                                    class="form-control form-contro-sm form-field display-flex"
                                    :placeholder="
                                        this.$func.__(
                                            'Internal Information',
                                            'erp'
                                        )
                                    "
                                ></textarea>
                            </td>
                        </tr>
                        <tr class="add-attachment-row inline-edit-row">
                            <td colspan="9" style="text-align: left">
                                <div class="attachment-container">
                                    <label class="col--attachement">{{
                                        this.$func.__("Attachment", "erp")
                                    }}</label>
                                    <file-upload
                                        v-model="attachments"
                                        url="/invoices/attachments"
                                    />
                                </div>
                            </td>
                        </tr>
                        <tfoot>
                            <tr class="inline-edit-row">
                                <td colspan="9" style="text-align: right">
                                    <combo-button :options="createButtons" />
                                </td>
                            </tr>
                        </tfoot>
                    </table>
                </div>
            </div>
        </form>
    </div>
</template>

<script>
export default {
    components: {
        SelectAccounts: window.$func.fetchComponent(
            "components/select/SelectAccounts.vue"
        ),
        SelectPeople: window.$func.fetchComponent(
            "partner/widgets/SelectPeople.vue"
        ),
        Datepicker: window.$func.fetchComponent(
            "components/base/Datepicker.vue"
        ),
        FileUpload: window.$func.fetchComponent(
            "components/base/FileUpload.vue"
        ),
        MultiSelect: window.$func.fetchComponent(
            "componentss/select/MultiSelect.vue"
        ),
        CheckFields: window.$func.fetchComponent(
            "components/check/CheckFields.vue"
        ),
        ShowErrors: window.$func.fetchComponent(
            "components/base/ShowErrors.vue"
        ),
        ComboButton: window.$func.fetchComponent(
            "components/select/ComboButton.vue"
        ),
    },

    data() {
        return {
            basic_fields: {
                people: {},
                trn_ref: "",
                payment_date:
                    this.$mybizna_var
                        .current_date /* global this.$mybizna_var */,
                deposit_to: "",
                billing_address: "",
                trn_by: "",
            },

            check_data: {
                bank_name: "",
                payer_name: "",
                check_no: "",
            },

            createButtons: [
                { id: "save", text: this.$func.__("Save", "erp") },
                {
                    id: "new_create",
                    text: this.$func.__("Save and New", "erp"),
                },
                { id: "draft", text: this.$func.__("Save as Draft", "erp") },
            ],

            form_errors: [],
            pay_bills: [],
            attachments: [],
            dueAmounts: [],
            totalAmounts: [],
            pay_methods: [],
            finalTotalAmount: 0,
            particulars: "",
            isWorking: false,
            accts_by_chart: [],
            acct_assets: this.$mybizna_var.acct_assets,
        };
    },

    computed: {
        ...mapState({ actionType: (state) => state.combo.btnID }),
    },

    emits: {
        // Validate submit event
        "remove-row": ({ index }) => {
            this.$delete(this.pay_bills, index);
            this.updateFinalAmount();
            return true;
        },
    },
    created() {
        this.getPayMethods();

        // initialize combo button id with `save`
        this.$store.dispatch("combo/setBtnID", "save");
    },

    mounted() {
        this.basic_fields.people = {
            id: parseInt(this.$route.params.vendor_id),
            name: this.$route.params.vendor_name,
        };
    },

    methods: {
        getPayMethods() {
            window.axios
                .get("/transactions/payment-methods")
                .then((response) => {
                    this.pay_methods = response.data;
                })
                .catch((error) => {
                    throw error;
                });
        },

        setCheckFields(check_data) {
            this.check_data = check_data;
        },

        getDueBills() {
            this.pay_bills = [];

            const peopleId = this.basic_fields.people.id;
            let idx = 0;
            let finalAmount = 0;

            if (isNaN(peopleId)) {
                return;
            }

            window.axios
                .get(`/bills/due/${peopleId}`)
                .then((response) => {
                    response.data.forEach((element) => {
                        if (element.due !== null && element.due > 0) {
                            this.pay_bills.push({
                                id: element.id,
                                voucher_no: element.voucher_no,
                                due_date: element.due_date,
                                amount: parseFloat(element.amount),
                                due: parseFloat(element.due),
                            });
                        }
                    });
                })
                .then(() => {
                    this.pay_bills.forEach((element) => {
                        this.totalAmounts[idx++] = parseFloat(element.due);
                        finalAmount += parseFloat(element.due);
                    });

                    this.finalTotalAmount = parseFloat(finalAmount).toFixed(2);
                });
        },

        getPeopleAddress() {
            const people_id = this.basic_fields.people.id;

            if (isNaN(people_id)) {
                return;
            }

            window.axios.get(`/people/${people_id}`).then((response) => {
                const billing = response.data;

                let street_1 = billing.street_1 ? billing.street_1 + "," : "";
                let street_2 = billing.street_2 ? billing.street_2 : "";
                let city = billing.city ? billing.city : "";
                let state = billing.state ? billing.state + "," : "";
                let postal_code = billing.postal_code
                    ? billing.postal_code
                    : "";
                let country = billing.country ? billing.country : "";

                const address = `${street_1} ${street_2} \n${city} \n${state} ${postal_code} \n${country}`;

                this.basic_fields.billing_address = address;
            });
        },

        updateFinalAmount() {
            let finalAmount = 0;

            for (let idx = 0; idx < this.totalAmounts.length; idx++) {
                finalAmount += parseFloat(this.totalAmounts[idx]);
            }

            this.finalTotalAmount = parseFloat(finalAmount).toFixed(2);
        },

        SubmitForPayment() {
            this.validateForm();

            if (this.form_errors.length) {
                window.scrollTo({
                    top: 10,
                    behavior: "smooth",
                });
                return;
            }

            this.pay_bills.forEach((element, index) => {
                element["amount"] = parseFloat(this.totalAmounts[index]);
            });

            let trn_status = null;
            if (this.actionType === "draft") {
                trn_status = 1;
            } else {
                trn_status = 4;
            }

            let deposit_id = this.basic_fields.deposit_to.id;

            if (this.basic_fields.trn_by.id === 4) {
                deposit_id = this.basic_fields.deposit_to.people_id;
            }

            window.axios
                .post("/pay-bills", {
                    vendor_id: this.basic_fields.people.id,
                    ref: this.basic_fields.trn_ref,
                    trn_date: this.basic_fields.payment_date,
                    due_date: this.basic_fields.due_date,
                    bill_details: this.pay_bills,
                    attachments: this.attachments,
                    type: "pay_bill",
                    status: trn_status,
                    particulars: this.particulars,
                    deposit_to: deposit_id,
                    trn_by: this.basic_fields.trn_by.id,
                    check_no: parseInt(this.check_data.check_no),
                    name: this.check_data.payer_name,
                    bank: this.check_data.bank_name,
                })
                .then((res) => {
                    this.showAlert(
                        "success",
                        this.$func.__("Pay-Bill Created!", "erp")
                    );

                    if (
                        this.actionType === "save" ||
                        this.actionType === "draft"
                    ) {
                        this.$router.push({ name: "Expenses" });
                    } else if (this.actionType === "new_create") {
                        this.resetFields();
                    }
                })
                .catch((error) => {
                    this.showAlert(
                        "error",
                        this.$func.__("Something went wrong!", "erp")
                    );
                    throw error;
                })
                .then(() => {
                    this.resetFields();
                    this.isWorking = false;
                });
        },

        changeAccounts() {
            this.accts_by_chart = [];
            if (
                this.basic_fields.trn_by.id === "2" ||
                this.basic_fields.trn_by.id === "3"
            ) {
                window.axios.get("/ledgers/bank-accounts").then((response) => {
                    this.accts_by_chart = response.data;
                    this.accts_by_chart.forEach((element) => {
                        if (
                            !Object.prototype.hasOwnProperty.call(
                                element,
                                "balance"
                            )
                        ) {
                            element.balance = 0;
                        }
                    });
                });
            } else if (this.basic_fields.trn_by.id === "1") {
                window.axios.get("/ledgers/cash-accounts").then((response) => {
                    this.accts_by_chart = response.data;
                    this.accts_by_chart.forEach((element) => {
                        if (
                            !Object.prototype.hasOwnProperty.call(
                                element,
                                "balance"
                            )
                        ) {
                            element.balance = 0;
                        }
                    });
                });
                /* globalmybizna_reimbursement_var */
            } else if (this.basic_fields.trn_by.id === "4") {
                if (
                   mybizna_reimbursement_var.reimbursement_module !==
                        "undefined" &&
                   mybizna_reimbursement_var.reimbursement_module === "1"
                ) {
                    window.axios
                        .get("/people-transactions/balances")
                        .then((response) => {
                            this.accts_by_chart = response.data;
                            this.accts_by_chart.forEach((element) => {
                                if (
                                    !Object.prototype.hasOwnProperty.call(
                                        element,
                                        "balance"
                                    )
                                ) {
                                    element.balance = 0;
                                }
                            });
                        });
                }
            }
            this.$root.$emit("account-changed");
        },

        validateForm() {
            this.form_errors = [];

            if (
                !Object.prototype.hasOwnProperty.call(
                    this.basic_fields.people,
                    "id"
                )
            ) {
                this.form_errors.push(
                    this.$func.__("People Name is required.", "erp")
                );
            }

            if (!this.basic_fields.payment_date) {
                this.form_errors.push(
                    this.$func.__("Transaction Date is required.", "erp")
                );
            }

            if (
                !Object.prototype.hasOwnProperty.call(
                    this.basic_fields.deposit_to,
                    "id"
                )
            ) {
                this.form_errors.push(
                    this.$func.__("Transaction Account is required.", "erp")
                );
            }

            if (
                !Object.prototype.hasOwnProperty.call(
                    this.basic_fields.trn_by,
                    "id"
                )
            ) {
                this.form_errors.push(
                    this.$func.__("Payment Method is required.", "erp")
                );
            }

            if (
                parseFloat(this.basic_fields.deposit_to.balance) <
                parseFloat(this.finalTotalAmount)
            ) {
                this.form_errors.push(
                    this.$func.__(
                        "Not enough balance in selected account.",
                        "erp"
                    )
                );
            }

            if (!parseFloat(this.finalTotalAmount)) {
                this.form_errors.push(
                    this.$func.__("Total amount can't be zero.", "erp")
                );
            }
        },

        showPaymentModal() {
            this.getDueBills();
        },

        resetFields() {
            this.basic_fields = {
                people: { id: null, name: null },
                trn_ref: "",
                payment_date: this.$mybizna_var.current_date,
                deposit_to: "",
                billing_address: "",
                trn_by: "",
            };

            this.check_data = {
                bank_name: "",
                payer_name: "",
                check_no: "",
            };

            this.form_errors = [];
            this.attachments = [];
            this.dueAmounts = [];
            this.totalAmounts = [];
            this.finalTotalAmount = 0;
            this.particulars = "";
            this.isWorking = false;

            // initialize combo button id with `save`
            this.$store.dispatch("combo/setBtnID", "save");
        },

        removeRow(index) {
            this.$delete(this.pay_bills, index);
            this.$delete(this.totalAmounts, index);
            this.updateFinalAmount();
        },
    },

    watch: {
        finalTotalAmount(newval) {
            this.finalTotalAmount = newval;
        },

        "basic_fields.people"() {
            this.getPeopleAddress();
        },

        "basic_fields.trn_by"() {
            this.changeAccounts();
        },
    },
};
</script>

